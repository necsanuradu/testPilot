import React, { useState, useEffect } from "react";
import testPilot from "../testPilot/test-pilot";

const SomeComponentForTestPilotNoTest = (props) => {
  testPilot.testSuite = {
    xTest: [{ render: ["true"] }],
  };
  const [color, setColor] = useState(props.color);
  const [size, setSize] = useState(props.size);
  useEffect(() => {
    setSize(props.size);
  });
  return testPilot.render(
    <div>
      <div className="card mx-5 bg-light">
        <div className="card-header">{color}</div>
        <div className="card-body">{size}</div>

        <div className="card-footer" test-suite="xTest">
          component SomeComponentForTestPilot, <br /> not tested, tests will be
          generated by testPilot
        </div>
      </div>
    </div>
  );
};

export default SomeComponentForTestPilotNoTest;
